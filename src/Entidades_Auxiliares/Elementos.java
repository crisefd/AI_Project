package Entidades_Auxiliares;

/**
 * Interfaz de los valores constantes de los elementos dentro de los algoritmos
 *
 * @author Cristhian Fuertes 
 */
public interface Elementos {

    /**
     * Entero que representa la presencia del agente (sorting robot) en una
     * matriz númerica
     */
    public final int ROBOT = -1;
    /**
     * Entero que representa la presencia del objeto 1 en una matriz númerica
     */
    public final int OBJ1 = -2;
    /**
     * Entero que representa la presencia del objeto 2 en una matriz númerica
     */
    public final int OBJ2 = -3;
    /**
     * Entero que representa la presencia del lugar 1 en una matriz númerica
     */
    public final int LUG1 = -4;
    /**
     * Entero que representa la presencia del lugar 2 en una matriz númerica
     */
    public final int LUG2 = -5;
    /**
     * Entero que representa la no presencia de algun elemento en una matriz
     * númerica
     */
    public final int LIBRE = 0;
}
